package br.edu.up.modelos;

public class seguroAutomovel {
    private Double valorFranquia;
    private Integer diasCarroReserva;
    private String coberturaVidros;
    
    public Double getValorFranquia() {
        return valorFranquia;
    }
    public void setValorFranquia(Double valorFranquia) {
        this.valorFranquia = valorFranquia;
    }
    public Integer getDiasCarroReserva() {
        return diasCarroReserva;
    }
    public void setDiasCarroReserva(Integer diasCarroReserva) {
        this.diasCarroReserva = diasCarroReserva;
    }
    public String getCoberturaVidros() {
        return coberturaVidros;
    }
    public void setCoberturaVidros(String coberturaVidros) {
        this.coberturaVidros = coberturaVidros;
    }

    
}
